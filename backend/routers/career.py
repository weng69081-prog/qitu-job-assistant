_USER_CAREERS_FILE = _os.path.join(_os.path.dirname(_os.path.dirname(__file__)), "data", "user_added_careers.json")

def _load_user_careers():
    """加载用户新增的AI生成职业"""
    if not _os.path.isfile(_USER_CAREERS_FILE):
        return []
    try:
        with open(_USER_CAREERS_FILE, "r", encoding="utf-8") as f:
            return json.load(f)
    except:
        return []

def _save_user_careers(careers):
    """保存用户新增职业列表"""
    _os.makedirs(_os.path.dirname(_USER_CAREERS_FILE), exist_ok=True)
    with open(_USER_CAREERS_FILE, "w", encoding="utf-8") as f:
        json.dump(careers, f, ensure_ascii=False, indent=2)

def _find_career_across_all(name):
    """在所有数据源中查找职业（CAREER_DB + user_added）"""
    name_lower = name.lower().strip()
    for cat, lst in CAREER_DB.items():
        for c in lst:
            if c["career"].lower() == name_lower:
                return dict(c)
    for c in _load_user_careers():
        if c["career"].lower() == name_lower:
            return dict(c)
    return None

def _search_careers_all(query, major_category=""):
    """在所有数据源中搜索职业，跨大类"""
    q = query.lower().strip() if query else ""
    results = []
    # 先搜 CAREER_DB
    for cat, lst in CAREER_DB.items():
        if major_category and cat != major_category:
            continue
        for c in lst:
            if not q:
                results.append(dict(c))
            elif (q in c["career"].lower() or
                  any(q in k.lower() for k in c.get("keywords", []) if k) or
                  any(q in r.lower() for r in c.get("responsibilities", []) if r)):
                results.append(dict(c))
    # 再搜 user_added
    for c in _load_user_careers():
        if major_category and c.get("major_category", "") != major_category:
            continue
        if not q or (q in c["career"].lower() or
                     any(q in k.lower() for k in c.get("keywords", []) if k) or
                     any(q in r.lower() for r in c.get("responsibilities", []) if r)):
            item = dict(c)
            item["ai_generated"] = True
            item["source"] = "user_added"
            results.append(item)
    return results

