@router.post("/match-companies")
def match_companies(
    careers: str = Query(...),
    city: str = "",
    education: str = "本科",
    skills: str = "",
    salary: str = "",
    job_type: str = "全职",
):
    """投递分析：按目标职业+城市+学历匹配企业，输出三级分层"""
    target_careers = [c.strip() for c in careers.split(",") if c.strip()][:3]
    
    # 聚合所有企业
    all_companies = []
    for cat_companies in COMPANY_DB.values():
        for co in cat_companies:
            all_companies.append(dict(co))
    
    # 筛选招目标职业的企业
    matched = []
    for co in all_companies:
        hires = co.get("hires", [])
        if not hires:  # 没有hires字段的企业不参与匹配
            continue
        for tc in target_careers:
            if tc in hires:
                if co not in matched:
                    matched.append(co)
                break
    
    if not matched:
        # 宽松匹配：招人范围覆盖任何目标职业关键词
        for co in all_companies:
            hires = co.get("hires", [])
            for tc in target_careers:
                if any(tc in h or h in tc for h in hires):
                    if co not in matched:
                        matched.append(co)
                        break
    
    # 城市薪资系数
    city_ratio = {
        "北京":1.0,"上海":1.0,"深圳":0.95,"杭州":0.85,"广州":0.85,
        "成都":0.7,"武汉":0.7,"南京":0.75,"西安":0.65,"郑州":0.6,
        "长沙":0.6,"合肥":0.6,"苏州":0.8,"重庆":0.65,"天津":0.7,
        "青岛":0.65,"大连":0.65,"厦门":0.65,"福州":0.6,"济南":0.65,
    }.get(city, 0.7)
    
    edu_tier = {"博士":"high","硕士":"high","本科":"mid","大专":"low","高中":"low"}.get(education, "mid")
    
    def score(co):
        s = 50  # 基准分
        # 城市匹配
        co_cities = co.get("city", "").split("/")
        if city in co_cities:
            s += 40
            reason_parts = [f"{city}有岗"]
        elif "全国" in co.get("city", ""):
            s += 20
            reason_parts = ["全国招聘"]
        else:
            reason_parts = ["非本地"]
        
        # 学历匹配
        if edu_tier == "low":
            # 高中/大专：优先本地+中小企业
            if co.get("tier","") in ["本地知名","创业公司","外包/中大型","中大型"]:
                s += 30
                reason_parts.append("学历匹配")
            else:
                s -= 10
        elif edu_tier == "high":
            if co.get("tier","") in ["头部大厂","四大","央企","央企/上市公司"]:
                s += 30
                reason_parts.append("冲刺可选")
            else:
                s += 10
        
        # 薪资匹配
        try:
            co_sal = co.get("campus_salary", "0")
            co_range = co_sal.replace("K","").split("-")
            co_min = int(co_range[0]) * 1000
            user_range = salary.replace("K","").replace("+","-999").split("-")
            user_low = int(user_range[0]) * 1000
            if co_min <= user_low * 1.5:
                s += 15
        except:
            pass
        
        co["recommend_reason"] = "，".join(reason_parts)
        co["_rs"] = min(s, 99)
        return s
    
    for co in matched:
        score(co)
    
    matched.sort(key=lambda x: x["_rs"], reverse=True)
    
    high = [c for c in matched if c["_rs"] >= 80][:6]
    potential = [c for c in matched if 60 <= c["_rs"] < 80][:6]
    backup = [c for c in matched if c["_rs"] < 60][:6]
    
    return {
        "careers": target_careers,
        "city": city,
        "city_ratio": round(city_ratio, 2),
        "summary": f"基于{len(target_careers)}个职业 · {city} · {education}，匹配到{len(matched)}家企业",
        "tiers": {
            "high": {"label": "🎯 高概率", "desc": "城市有岗、学历匹配、薪资契合，建议优先投递", "items": high},
            "potential": {"label": "🚀 可争取", "desc": "部分条件匹配，补足后成功率提升", "items": potential},
            "backup": {"label": "📋 冲刺/练手", "desc": "门槛较高或非本地，可作为备选或面试练习", "items": backup},
        }
    }

