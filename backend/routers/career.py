@router.post("/save-path")
def save_career_path(data: dict = Body({})):
    """生成并保存收藏职业的成长路线"""
    career = data.get("career", "")
    if not career:
        return {"error": "缺少职业名称"}
    # 先调 MiMo 生成
    prompt = f'''你是大学职业规划导师。请为想成为「{career}」的大一新生，制定一份从大一到大四共7个学期的专属学习成长路线，越详细越好。

请返回JSON数组（7个学期，不要markdown，不要备注）：
[
  {{
    "phase": "大一上/大一下/大二上/大二下/大三上/大三下/大四上",
    "goals": "本阶段核心目标，30字内",
    "courses": ["课程1", "课程2", "课程3", "课程4"],
    "tasks": ["具体任务1", "具体任务2", "具体任务3", "具体任务4", "具体任务5"],
    "certificates": ["推荐考的证书1", "证书2"],
    "resources": ["推荐学习资源1（如某某书/某某课）", "资源2"],
    "video_keywords": ["搜索关键词1", "搜索关键词2", "搜索关键词3"],
    "tips": "小贴士，15字内"
  }}
]

要求：
- 课程要贴合{career}岗位核心技能
- 任务必须具体可执行（如「完成XX项目」「学完XX课程」「刷XXX道题」）
- certificates字段推荐该阶段适合考的证书（如「计算机二级」「软考」「AWS认证」等）
- resources字段推荐具体书籍、在线课程、网站等学习资源
- **video_keywords字段**：为每个学期推荐2-3个B站可搜索到的视频学习关键词，要贴合该学期的课程和任务，让大一新生能搜到对应的入门教程（如"Python入门教程"、"前端HTML基础"等具体的课程名称）
- 只返回JSON数组，不要其他文字'''
    from routers.llm import chat
    text = chat(prompt, system="你是大学职业规划专家", max_tokens=2500)
    path = []
    try:
        text = text.strip()
        if text.startswith("```"):
            text = text.split("\n", 1)[1]
            if text.endswith("```"):
                text = text[:-3]
        path = json.loads(text) if isinstance(json.loads(text), list) else []
    except:
        pass
    if not path:
        cat = ""
        for c, careers_list in CAREER_DB.items():
            if any(cc["career"] == career for cc in careers_list):
                cat = c
                break
        path = GROWTH_PATH_DB.get(cat, GROWTH_PATH_DB.get("计算机类", []))
    # 保存到文件
    _ensure_path_dir()
    data_file = {"career": career, "path": path, "saved_at": __import__("datetime").datetime.now().isoformat()}
    with open(_os.path.join(_PATH_DIR, f"{career}.json"), "w", encoding="utf-8") as f:
        json.dump(data_file, f, ensure_ascii=False)
    return {"career": career, "path": path}

@router.get("/saved-path/{career_name}")
def get_saved_path(career_name: str):
    """获取已保存的成长路线"""
    _ensure_path_dir()
    fpath = _os.path.join(_PATH_DIR, f"{career_name}.json")
    if _os.path.isfile(fpath):
        with open(fpath, "r", encoding="utf-8") as f:
            data = json.load(f)
            return {"career": career_name, "path": data.get("path", []), "saved": True}
    return {"career": career_name, "path": [], "saved": False}